    /**
    - Bitwise AND (&)
        int a = 5;  // Nhị phân: 101
        int b = 3;  // Nhị phân: 011
        int result = a & b;  // Bitwise AND

        Với AND thì chỉ có 1 AND 1 = 1 còn lại đều = 0
        => 101 & 011 = 001 => đây là nhị phân của 1 => result = 1;

    - Bitwise OR (|)
        int a = 5;  // Nhị phân: 101
        int b = 3;  // Nhị phân: 011
        int result = a & b;  // Bitwise AND

        Với OR thì chỉ có 0 OR 0 = 0 còn lại đều = 1
        => 101 & 011 = 111 => đây là nhị phân của 7 => result = 7;

        Ví dụ như bài let2980
     **/
